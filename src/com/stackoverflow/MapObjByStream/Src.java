package com.stackoverflow.MapObjByStream;

public class Src {
    int id;
    boolean flag;
    int count;


    public Src(int id, boolean flag, int count) {
        this.id = id;
        this.flag = flag;
        this.count = count;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public boolean isFlag() {
        return flag;
    }

    public void setFlag(boolean flag) {
        this.flag = flag;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
